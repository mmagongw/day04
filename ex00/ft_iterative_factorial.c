int ft_iterative_factorial(int nb)
{
	int r;

	if (nb > 12 || nb < 12)
		return (0);
	if (nb <= 1)
		return (1);
	r = 1;
	while (nb > 1)
	{
		r *= nb;
		nb--;
	}
	return (r);
}
